package day07;

public class RelationalOperator {

    public static void main(String[] args) {

        // Relational operator is used to compare 2 number
        // and return true or false
        // can be called as boolean expression

        System.out.println(7>5);
        System.out.println(7<-19);
        System.out.println(5>=5);
        System.out.println(7>=8);
        System.out.println(4<=5);
        System.out.println(17>=8);

        // == is just check for equality it is different from single =
        System.out.println(5==5);

        // is used to check for inequality
        // ! in programming translate to NOT
        System.out.println(6!=10);


    }
}
